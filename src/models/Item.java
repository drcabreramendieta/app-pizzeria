package models;

/**
 *
 * @author hola
 */
public class Item {
    private String name;
    private double value;

    public Item(String name, double value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public double getValue() {
        return value;
    }
    
    @Override
    public String toString(){
        return name;
    }
}
